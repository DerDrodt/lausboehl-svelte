<section>
	<h2>Schützt unsere Äcker!</h2>

	<article>
		<p>
			Ackerland ist nicht nur eine grüne nutzlose Wiese. Nein, es ist eine Fläche auf der in der
			Region Nahrungs- und Futtermittel für die Region angepflanzt wird. Diese Erkenntnis hat sich
			noch nicht überall herum gesprochen. In Heppenheim gab es zu diesem Thema eine historische
			Pressekonferenz. Kreisvorsitzende vom Bund und Regionalbauernverband haben nun gemeinsam ihre
			Stimme erhoben und schlagen Alarm. Es geht nicht „nur“ um Ackerfläche, durch jede Versiegelung
			werden auch landwirtschaftliche Betriebe in ihrer Existenz bedroht. Außerdem führt die
			schwindende Erzeugung von Nahrungs- und Futtermitteln innerhalb Deutschlands zur Verlagerung
			der Produktion in Entwicklungsländer mit verheerenden Folgen auf die dortigen Menschen und
			Natur.
		</p>

		<p>
			Pro Tag gehen 2 Hektar Fläche, das entspricht fast der Größe des Lausböhls, in Südhessen
			verloren. Das bedeutet das in 83 Jahren alle Flächen aufgebraucht sein werden. Dann gibt es
			den Kreisbauernmarkt nicht mehr und alles was wir und unsere Tiere brauchen, müssen wir
			einfliegen oder mit LKWs anfahren lassen. Ist dieser Trend nicht genau gegenläufig zu den
			angeblich so nachhaltig einkaufenden Kunden, die regionale und saisonale Produkte möglichst
			aus Bioanbau kaufen wollen? Wann begreifen wir endlich, dass nichts an Ressourcen unendlich
			ist in unserer Welt?
		</p>

		<p>
			Und wo werden wir dann unsere Erholung suchen? Einen Spaziergang mit oder ohne Hund durch die
			Feldwege am Ortsrand wird es nicht mehr geben! Joggen, Nordic Walking und Radfahren durch das
			tolle Gewerbegebiet mit Feinstaubbeigabe der LKWs, ein ganz neues Naturerlebnis!
		</p>

		<p>
			Wollen wir wirklich so unser Südhessen unseren Kindern überlassen? Nein, es reicht, wir müssen
			umdenken, damit für die nachfolgenden Generationen noch Natur übrig bleibt!
		</p>
	</article>
</section>
