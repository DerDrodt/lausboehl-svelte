<section>
	<h2>Faktenlage</h2>

	<article>
		<aside class="map">
			<img src="/images/Rechnung_LKW-Fahrten.jpg" alt="Rechnung" style="width:100%;" />
		</aside>
		<h3>Was erwartet uns in der Kreisstadt Groß-Gerau an zukünftigem Verkehr?</h3>
		<p>
			Im März 2016 hat Bürgermeister Stefan Sauer von Firma Altmann erfahren, dass nach dem Ausbau
			mit einer wahrscheinlichen Steigerung des Fahrzeugumlaufs auf 116.100 Umsetzungen im Jahr zu
			rechnen ist. Dies ist zu lesen im
			<a
				href="http://www.griesheimeranzeiger.de/images/stories/archiv/gr/2016/GR_09_2016.pdf"
				target="_blank"
				rel="extern">Gerauer Rundblick am 3. März 2016</a
			>.
		</p>
		<p>
			In den Gutachten aus 2015 ist lediglich von 20.000 Umsetzungen pro Jahr nur für die erweiterte
			Fläche(!) zu rechnen.
		</p>

		<h3>Was bedeutet nun 116.100 PKW-Umsetzungen pro Jahr?</h3>
		<p>
			Bei einer durchschnittlichen Beladung von 4,4 PKW pro Spezialtransporter (ermittelt von der BI
			am <a href="/chronik#Zaehlung">9.5.2016</a>) ergeben sich ca. 26.000 Fahrten von
			Spezialtransportern pro Jahr in einfacher Richtung. Dies muss noch mit einem Faktor 2
			multipliziert werden (An- und Abtransport). Somit ergeben sich in Summe ca. 50.000 Fahrten von
			Spezialtransportern pro Jahr. Bei einer Annahme von 250 Arbeitstagen müssen wir mit ca. 200
			Spezialtransportern am Tag rechnen (also bei 10h sind dies alle 3 Minuten ein
			Spezialtransporter!).
		</p>

		<p>
			Im Ausschuss PLUS (Planung, Umwelt und Stadtentwicklung) am 18.5.2016 hat der Prokurist der
			Firma Altmann, Herr Yorck Hellwig, bestätigt, dass es selbstverständlich auch Leerfahrten
			gibt. Den von der BI ermittelten durchschnittlichen Beladungswert von 4,4 konnte er nicht
			bestätigen, hat diesen aber auch nicht als unkorrekt bezeichnet.
		</p>
		<p>
			Die Gutachten aus 2015 rechnen jedoch die Anzahl der Fahrten mit der Annahme, dass die LKWs
			immer mit 8 PKWs voll beladen sind! Genau diesen Unterschied haben wir in unseren
			Widersprüchen aufgeführt.
		</p>
	</article>
</section>

