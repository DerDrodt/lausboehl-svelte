	<p>
		Die Firma ARS Altmann beabsichtigt, ihr Firmengelände in <strong>Groß-Gerau Dornberg</strong> zu
		erweitern, um darauf noch mehr KFZs abstellen zu können. Dazu sollen
		<strong>1,6 ha Grünfläche</strong> auf der Gemarkung "Am Lausböhl" versiegelt werden.
	</p>

	<h3>Die Situation</h3>

	<p>
		Die Planunterlagen für das Vorhaben der Firma Altmann mit entsprechenden Gutachten wurden im
		Juli 2015 offengelegt. Alle Anwohner haben fristgerecht <a href="#flyer">Widerspruch</a> eingelegt.
		Da offensichtlich keiner der Verantwortlichen in der Nähe wohnt oder auch selbst betroffen ist, werden
		wichtige Entscheidungen am Schreibtisch getroffen beziehungsweise ortsfremde Gutachter für viel Geld
		beauftragt.
	</p>

	<h3>Flyer und Widersprüche</h3>

	<p>
		Was aus den Gutachten nicht hervorgeht, ist die Tatsache, dass mit der Erweiterung der KFZ-
		Abstellfläche auch eine Erhöhung der Frequenz des be- und entladenden LKW-Verkehrs einhergeht:
		In Zukunft sollen also noch mehr LKWs über die B44 rollen! Von dem damit verbundenen Lärm, der
		Luftverschmutzung oder einer weiteren Vernässung der Region durch die Versiegelung ist in keinem
		der Gutachten die Rede! Schon jetzt haben einige Anwohner feuchte Keller.
	</p>

